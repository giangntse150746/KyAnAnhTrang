function triggerReveal(previewContainer) {
    // get the id's value of the preview-image
    var previewValue = previewContainer.getElementsByTagName("img")[0].id;
    console.log(previewValue);
    switch (previewValue) {
        case "HuyenLinh":
            reveal(revealHuyenLinh);
            break;
        case "TranDong":
            reveal(revealTranDong);
            break;
        case "AnNhien":
            reveal(revealAnNhien);
            break;
        case "NguoiGia":
            reveal(revealNguoiGia);
            break;
        case "TruongLam":
            reveal(revealTruongLam);
            break;
        case "HaVi":
            reveal(revealHaVi);
            break;
        case "BaoTran":
            reveal(revealBaoTran);
            break;
        case "HuyenKhanh":
            reveal(revealHuyenKhanh);
            break;
    }
}


